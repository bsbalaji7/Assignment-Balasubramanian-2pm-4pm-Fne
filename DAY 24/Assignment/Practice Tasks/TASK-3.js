// Difference between Runtime Error and Logical Error (More Points)

// Runtime Error occurs during program execution.

// It stops the normal flow of the program.

// It is caused by invalid operations like undefined variables or wrong function calls.

// Error message is shown in the console.

// Logical Error occurs due to incorrect program logic.

// Program executes without crashing.

// It produces incorrect or unexpected output.

// No error message is shown in the console.